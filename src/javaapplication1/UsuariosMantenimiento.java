public ArrayList<USUARIOS>UsuarioList() throws SQLException
{
    ArrayList<USUARIOS>UsuarioList = new ArrayList<USUARIOS>();
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/mysql","root", "1998naciojose");
    Statement st = conn.createStatement();
    ResultSet rs = st.executeQuery("SELECT * FROM mydb.USUARIOS;");
    USUARIOS usuario;
    while(rs.next()){
        usuario=new USUARIOS(rs.getString("USUARIO"),
                rs.getString("DESCRIPCION"),
                rs.getDate("FECHA_CREACION"),
                rs.getDate("FECHA_MODIFICACION"),rs.getString("USUARIO_CREADOR"),rs.getString("USUARIO_MODIFICADOR"),rs.getString("EMPLEADOS_ID_EMPLEADOS"));
        UsuarioList.add(usuario);
    }
    return UsuarioList;
}

public void show_usuario() throws SQLException{
    ArrayList<USUARIOS> list = UsuarioList();
    DefaultTableModel model= (DefaultTableModel)jTable1.getModel();
    Object[] row = new Object[12];
    for(int i=0 ;i<list.size();i++){
        row[0]=list.get(i).getUsuario();
        row[1]=list.get(i).getDescripcion();
        row[2]=list.get(i).getFecha_creacion();
        row[3]=list.get(i).getFecha_modificacion();
        row[4]=list.get(i).getUsuario_creador();
        row[5]=list.get(i).getUsuario_modificador();
        row[6]=list.get(i).getEmpleados_id_empleados();
        model.addRow(row);
    }
}
